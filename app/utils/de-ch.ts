const EXACT_TRANSLATIONS_BY_LOWERCASE = Object.fromEntries(
  Object.entries(EXACT_TRANSLATIONS).map(([source, translation]) => [
    source.toLowerCase(),
    translation,
  ])
);

const sortedFragments = Object.entries(FRAGMENT_TRANSLATIONS).sort(
  ([left], [right]) => right.length - left.length
);

function normalizeText(value: string) {
  return value.replace(/\s+/g, " ").trim();
}

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function preserveOuterWhitespace(original: string, translated: string) {
  const leadingWhitespace = original.match(/^\s*/)?.[0] ?? "";
  const trailingWhitespace = original.match(/\s*$/)?.[0] ?? "";

  return `${leadingWhitespace}${translated}${trailingWhitespace}`;
}

function preserveMatchCase(original: string, translated: string) {
  if (original.toUpperCase() === original) {
    return translated.toUpperCase();
  }

  if (original[0] && original[0] === original[0].toUpperCase()) {
    return translated.charAt(0).toUpperCase() + translated.slice(1);
  }

  return translated;
}

function translateFragments(value: string) {
  return sortedFragments.reduce((translated, [source, target]) => {
    const matcher = new RegExp(`\\b${escapeRegExp(source)}\\b`, "gi");

    return translated.replace(matcher, (match) =>
      preserveMatchCase(match, target)
    );
  }, value);
}

export function translateToDeCh(value: string) {
  const normalizedValue = normalizeText(value);
  if (!normalizedValue) return value;

  const exactTranslation =
    EXACT_TRANSLATIONS[normalizedValue as keyof typeof EXACT_TRANSLATIONS] ??
    EXACT_TRANSLATIONS_BY_LOWERCASE[normalizedValue.toLowerCase()];

  if (exactTranslation) {
    return preserveOuterWhitespace(value, exactTranslation);
  }

  for (const rule of REGEX_TRANSLATIONS) {
    const regularExpression = new RegExp(rule.source, rule.flags);
    if (regularExpression.test(normalizedValue)) {
      return preserveOuterWhitespace(
        value,
        normalizedValue.replace(regularExpression, rule.replacement)
      );
    }
  }

  const fragmentTranslation = translateFragments(normalizedValue);
  if (fragmentTranslation !== normalizedValue) {
    return preserveOuterWhitespace(value, fragmentTranslation);
  }

  return value;
}

export function translateBookingStatus(status: string) {
  return translateToDeCh(status);
}

export function translateRoleLabel(role: string) {
  return translateToDeCh(role);
}

export function translateNotificationContent(content: {
  title: string;
  message?: string | null;
}) {
  return {
    title: translateToDeCh(content.title),
    message: content.message
      ? translateToDeCh(content.message)
      : content.message,
  };
}

